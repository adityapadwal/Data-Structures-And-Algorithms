// In C++, #include<iostream> is a preprocessor directive that includes the standard input/output stream library 
// in your C++ program. The library allows you to perform input and output operations using the std::cin (for input) 
// and std::cout (for output) objects. It is a fundamental part of C++ programming and is often used for 
// simple input/output tasks, like displaying text or reading user input from the console.

// what is using namespace std in c++
